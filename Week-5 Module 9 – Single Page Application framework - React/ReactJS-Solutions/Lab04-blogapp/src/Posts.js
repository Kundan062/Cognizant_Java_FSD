import React from 'react';

// Lab 4: Class based component demonstrating componentDidMount()
// and componentDidCatch() life-cycle hooks
class Posts extends React.Component {
  constructor(props) {
    super(props);
    this.state = {
      posts: [],
      hasError: false
    };
  }

  loadPosts() {
    fetch('https://jsonplaceholder.typicode.com/posts')
      .then((response) => response.json())
      .then((data) => {
        this.setState({ posts: data.slice(0, 10) });
      })
      .catch((error) => {
        this.setState({ hasError: true });
      });
  }

  componentDidMount() {
    this.loadPosts();
  }

  componentDidCatch(error, info) {
    alert('An error occurred while rendering Posts: ' + error.message);
  }

  render() {
    if (this.state.hasError) {
      return <p>Something went wrong while loading posts.</p>;
    }
    return (
      <div>
        <h2>Blog Posts</h2>
        {this.state.posts.map((post) => (
          <div key={post.id}>
            <h3>{post.title}</h3>
            <p>{post.body}</p>
          </div>
        ))}
      </div>
    );
  }
}

export default Posts;
